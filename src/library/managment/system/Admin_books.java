    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        LibraryManagmentSystem lms = new LibraryManagmentSystem();
        Connection c = null;
        Statement s = null;
        String    i     = id.getText();
        String    n     = "'"+name.getText()+"'";
        String    au    = "'"+author.getText()+"'";
        String    month = mon.getText();
        String    day   = dd.getText();
        String    year  = yyyy.getText();
        String full_date= "'"+month+day+year+"'";
        String    desc  = "'"+description.getText()+"'";
        String    p = price.getText();
        String    q = quantity.getText();
        String    l = "'"+language.getText()+"'";
        String date_formate = "'"+"fxMONddyyyy"+"'";
        if(i.isEmpty()||n.isEmpty()||au.isEmpty()||month.isEmpty()||desc.isEmpty()||p.isEmpty()||q.isEmpty()||l.isEmpty()||day.isEmpty()||year.isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else{
            try {
                c = lms.connect();
                s = c.createStatement();
                int       ii = Integer.parseInt(i);
                int       pp = Integer.parseInt(p);
                int       qq = Integer.parseInt(q);
                int x = s.executeUpdate("insert into books values(" +i+ "," +n+ "," +au+ ", upper(TO_DATE("+full_date +","+ date_formate+ ")) ," +desc+ "," +p+ "," +q+ "," +l+ ")");
                if(x != 0){
                    JOptionPane.showMessageDialog(this, "1 Row Inserted");
                }else{
                    JOptionPane.showMessageDialog(this, "0 Row Inserted");
                }
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } finally {
                try {
                    s.executeUpdate("commit");
                    DefaultTableModel tm = (DefaultTableModel) t.getModel();
                    tm.setRowCount(0);
                    ResultSet rs = s.executeQuery("select * from books");
                    while(rs.next()){
                         String sp4[]= rs.getString(4).split(" ");
                    tm.addRow(new Object [] {rs.getInt(1),rs.getString(2),rs.getString(3),sp4[0],rs.getString(5),rs.getInt(6),rs.getInt(7),rs.getString(8)});
                    }
                    id.setText("ID");
                    name.setText("NAME");
                    author.setText("AUTHOR");
                    mon.setText("MON");
                    dd.setText("dd");
                    yyyy.setText("yyyy");
                    description.setText("DESCRIPTION");
                    price.setText("PRICE");
                    quantity.setText("QUANTITY");
                    language.setText("LANGUAGE");
                    s.close();
                    c.close();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());    
                }
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked


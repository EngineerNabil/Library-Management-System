    private void lbTitleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbTitleMouseClicked
        LibraryManagmentSystem lms = new LibraryManagmentSystem();
        Connection c = null;
        Statement s = null;
        String pp = "'"+TextComponent.getText()+"'";
        String nn = "'"+TextComponent2.getText()+"'";
        try {
            c = lms.connect();
            s = c.createStatement();
            int x       = s.executeUpdate("Update libraryan set password = "+pp+" where name ="+nn);
            if(x!=0){
                JOptionPane.showMessageDialog(this, "Password Changed");
                new Libraryan_login().setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(this, "User Not Found");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "ERORR");
        }finally{
            try {
                s.executeUpdate("commit");
                s.close();
                c.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "ERORR IN CLOSE");
            }
        }
    }//GEN-LAST:event_lbTitleMouseClicked


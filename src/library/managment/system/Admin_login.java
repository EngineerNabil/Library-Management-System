    private void lbTitleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbTitleMouseClicked
        LibraryManagmentSystem lms = new LibraryManagmentSystem();
        Connection c = null;
        Statement s = null;
        File      f = null;
        FileWriter fw =null;
        BufferedWriter bw  = null;
        String iii = TextComponent1.getText();
        int    ii  = Integer.parseInt(iii);
        String pp = "'"+TextComponent.getText()+"'";
        try {
            c = lms.connect( );
            s = c.prepareStatement(pp);
            s = c.createStatement();
            int x  = s.executeUpdate("select id , password from admins where password = "+pp+" and id ="+iii);
            f   = new File("login");
            fw  = new FileWriter(f);
            bw  = new BufferedWriter(fw);
            if(x != 0){        
                bw.write(""+ii+"");
                new Admin_home().setVisible(true);
                dispose();
            }else{
                JOptionPane jp = new JOptionPane();
               jp.showMessageDialog(this, "User Not Found");
               
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }finally{
            try {
                bw.close();
                fw.close();
                s.close();
                c.close();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "ERORR IN Finally");
            }
        }
    }//GEN-LAST:event_lbTitleMouseClicked


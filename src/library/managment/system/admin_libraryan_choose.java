    private void adminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_adminMouseClicked
        if(libraryan.isSelected()){
            libraryan.setSelected(false);
            admin.setSelected(true);
        }
    }//GEN-LAST:event_adminMouseClicked

    private void libraryanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_libraryanMouseClicked
        if(admin.isSelected()){
            admin.setSelected(false);
            libraryan.setSelected(true);
        }
    }//GEN-LAST:event_libraryanMouseClicked

